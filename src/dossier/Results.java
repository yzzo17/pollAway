    private void printPoll(){
        for (int i = 0; i < AdminUser.Ques.size(); i++) {
            JLabel q = new JLabel();
            q.setText(AdminUser.Ques.get(i).getQuestion());
            this.quesPanel.add(q);
            quesPanel.setLayout(new BoxLayout(quesPanel, BoxLayout.Y_AXIS));
            for (int j = 0; j < AdminUser.Ques.get(i).getAnswers().size(); j++) {
                JLabel a = new JLabel();
                a.setText("-"+AdminUser.Ques.get(i).getAnswers().get(j));
                this.quesPanel.add(a);
                quesPanel.setLayout(new BoxLayout(quesPanel, BoxLayout.Y_AXIS));
            }
        }
    }
    
    private void printCount(){
        
        
        for (int i = 0; i < AdminUser.results.size(); i++) {
            ArrayList<Answer> aArr = AdminUser.results.get(i);
            
            String q = "*";
            vCount.add(q);
            
            for (int j = 0; j < aArr.size(); j++) {
                ArrayList<User> uArr = aArr.get(j).users;
                
                String a = Integer.toString(uArr.size());
                vCount.add(a);
            }
        }

        for (int i = 0; i < vCount.size(); i++) {
            JLabel a = new JLabel();
            
            a.setText(vCount.get(i));
            votePanel.add(a);
            votePanel.setLayout(new BoxLayout(votePanel, BoxLayout.Y_AXIS));
        }
        
    }
    
    private void printDemo(){
        for (int j = 0; j < AdminUser.results.size(); j++) {
            ArrayList<Answer> ansArray = AdminUser.results.get(j);
            JLabel q = new JLabel();
            q.setText("*");
            demoPanel.add(q);
            
            for (int k = 0; k < ansArray.size(); k++) {
                Answer currentAns = ansArray.get(k);
                
                String demoline = "";
                for (int i = 0; i < AdminUser.demoArray.size(); i++) {
                    String currentDemo = AdminUser.demoArray.get(i);
                    String dcount = getDemo(currentDemo, currentAns);     
                    
                    demoline = demoline + currentDemo + " " + dcount+ " | ";
                }
                
                JLabel dl = new JLabel();
                dl.setText(demoline);
                demoPanel.add(dl);
                demoPanel.setLayout(new BoxLayout(demoPanel, BoxLayout.Y_AXIS));
                
            }    
        }
    }
    
    private String getDemo(String demoToCheck, Answer currentAns){
        
        double total = AdminUser.total;
        System.out.println(total);
        
        double demoCount = 0;
        for (int i = 0; i < currentAns.users.size(); i++) {                 //WORK HERE
            User currentUser = currentAns.users.get(i);
            
            String demo = currentUser.demographic;    
            if (demo.equals(demoToCheck)) {
                demoCount = demoCount + 1;
            }
        }
        double dc;
        
        if (total == 0) {
            return "0";
        }
        else{
            dc = (demoCount/total)*100;
            return Double.toString(dc)+"%";
        }
    }
